package com.mygdx.game.MainGame.Entity;

public class Hole {
    private float holeX;
    private float holeY;

    public Hole(float holeX, float holeY) {
        this.holeX = holeX;
        this.holeY = holeY;
    }

    public float getHoleX() {
        return holeX;
    }

    public float getHoleY() {
        return holeY;
    }

}
